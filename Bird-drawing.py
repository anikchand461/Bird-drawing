# Bird-drawing

from turtle import *
import turtle

t=Turtle()
s=Screen()
#s.bgcolor('blue')
t.speed(1)
t.hideturtle()
s.screensize(600,600)

#head,eye,beak---
t.up()
t.goto(-100,50)
t.down()
t.fillcolor('yellow')
t.begin_fill()
t.circle(50)
t.end_fill()
t.up()
t.goto(-110,110)
t.lt(90)
t.down()
t.fillcolor('black')
t.begin_fill()
t.circle(10)
t.end_fill()
t.up()
t.goto(-145,118)
t.down()
t.fillcolor('orange')
t.begin_fill()
t.lt(115)
t.fd(50)
t.lt(130)
t.fd(54)
t.lt(145)
for i in range(46):
    t.fd(1)
    t.rt(1)
t.end_fill()

#body---
t.up()
t.fillcolor('yellow')
t.goto(-80,55)
t.begin_fill()
t.seth(-76)
t.down()
t.circle(100,176)
t.seth(180)
t.fd(166)
t.seth(-92)
for i in range(61):
    t.fd(1)
    t.rt(1.2)
t.end_fill()

#wings----
t.up()
t.goto(-50,100)
t.fillcolor('orange')
t.begin_fill()
t.down()
t.seth(40)
t.fd(169)
t.rt(90)
for i in range(108):
    t.fd(1.1)
    t.rt(0.65)
t.end_fill()

t.up()
t.goto(-50,100)
t.fillcolor('orange')
t.begin_fill()
t.down()
t.seth(45)
t.fd(160)
t.rt(90)
for i in range(265):
    t.fd(1.1)
    t.rt(0.65)
t.seth(40)
t.circle(50,20)
t.end_fill()

#legs----

t.pensize(3)
t.up()
t.goto(5,-20)
t.down()
t.seth(-60)
t.fd(30)
t.rt(70)
t.fd(40)
t.rt(60)
t.fd(27)
t.rt(180)
t.fd(27)
t.rt(150)
t.fd(27)
t.rt(180)
t.fd(27)
t.rt(150)
t.fd(27)

t.up()
t.goto(45,-20)
t.down()
t.seth(-60)
t.fd(30)
t.rt(70)
t.fd(40)
t.rt(60)
t.fd(27)
t.rt(180)
t.fd(27)
t.rt(150)
t.fd(27)
t.rt(180)
t.fd(27)
t.rt(150)
t.fd(27)

turtle.done()
